package com.demo.control;

public class Switch {
	public static void main(String[] args) {
		String Banker = "Axis";
		
		switch(Banker) {
		case "Axis" : System.out.println("Nishant's account is in Axis bank.");
		break;
		case "HDFC" : System.out.println("Nishant's account is in HDFC bank.");
		break;
		case "Kotak" : System.out.println("Nishant's account is in Kotak Mahindra bank.");
		}
	}
}
